package day39_overriding;

public class Overriding {
/*
 Parent classtaki bir methodu child classa gore ozellestirerek kullanmadir.
 Overriding kurallari:
 1) Overriding creat edilirken method signature(name+parametre) kesinlikle
 degistirilemez.
 2) Overriding creat edilirken access modifier belirli kurakllara gore degistirilebilir.
 3) Overriding creat edilirken return type belirli kurallara gore degistirilebilir.
 4) Overriding creat edilirken method body %99 degistirilir ama degistirilmezse CTE 
 	vermez ama bosuna degistirmis oluruz.
 5) Overriding creat edilirken parent child relationship sarttir.
 ONEMLI: Inheritance olmadan overriding olmaz olamaz.	
 6) Overridden method ==> Parent class'daki method'dur(zavalli ezilen)       
  --> Overriding method ==> Child class'daki method'dur(zalim ezen !)   
 7) Overriding method (child) in Access modifier i ya Overridden method (parent) ile ayni 
 ya da daha genis olmali. Evlat babanin erisimini daraltamaz
 ama genisletebilir.
 8) Overriding method (child) in return type i void ise Overridden method(parent) un return
  type de void olmali. yoksa kabul etmez.
 9) Overriding method (child) in return type i Wrapper class tan ise Overridden method(parent)
  un return  type de Wrapper classs tan olmali. yoksa kabul etmez.
 
 
 
 
 
 
 
 */
}
